    private void botaoEscolherImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEscolherImagemActionPerformed
        int valorRetorno = escolherImagem.showOpenDialog(this);
        escolherImagem.setDialogTitle("Selecione a imagem");
        
        if(valorRetorno == JFileChooser.APPROVE_OPTION){
            try{
                imagem.setImagemOriginal(ImageIO.read(escolherImagem.getSelectedFile()));
                labelImagem.setIcon(resizeMaiores());
                caminho.setText("Nome: " + escolherImagem.getSelectedFile().getName());
            }catch(IOException e){
                caminho.setText("Nenhuma imagem escolhida");
                labelImagem.setIcon(null);
            }
        }else{
            caminho.setText("Nenhuma imagem escolhida");
            labelImagem.setIcon(null);
        }
        
    }//GEN-LAST:event_botaoEscolherImagemActionPerformed

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        if(imagem.getImagemEditada() != null){
            int valorRetorno = escolherImagem.showSaveDialog(this);

            escolherImagem.setDialogTitle("Selecione o diretorio");

            if(valorRetorno == JFileChooser.APPROVE_OPTION){
                try {
                    ImageIO.write(imagem.getImagemEditada(), "jpg", new File(escolherImagem.getSelectedFile().getAbsolutePath()));
                } catch (IOException ex) {
                    System.out.println("Failed to save image!");
                }
            }
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

