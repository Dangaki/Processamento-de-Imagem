    public ImageIcon resizeMaiores(){
        double scalax = (double) (labelImagem.getWidth()-15) / imagem.getImagemEditada().getWidth() ;
        double scalay = (double) (labelImagem.getHeight()-15) / imagem.getImagemEditada().getHeight() ;
        double resultado = Math.min(scalax, scalay);
       
            return new ImageIcon(imagem.getImagemEditada().getScaledInstance((int) (imagem.getImagemEditada().getWidth() * resultado), (int) (imagem.getImagemEditada().getHeight() * resultado), Image.SCALE_SMOOTH));
            
    }
     

